export function DashboardSidebar() {
  const pathname = usePathname();
  const { user } = useAuth();
  
  // Get role-specific links based on user role
  const getRoleSpecificLinks = () => {
    if (!user) return [];
    
    switch (user.role) {
      case 'farmer':
        return farmerLinks;
      case 'teacher':
        return teacherLinks;
      case 'admin':
        return adminLinks;
      default:
        return [];
    }
  };
  
  const roleSpecificLinks = getRoleSpecificLinks();
  
  return (
    <aside className="hidden md:block w-64 border-r min-h-screen pt-8">
      <div className="px-4">
        <nav className="space-y-1">
          {sidebarLinks.map((link) => (
            <Link
              key={link.href}
              href={link.href}
              className={cn(
                'flex items-center space-x-2 px-3 py-2 rounded-md text-sm font-medium transition-colors',
                pathname === link.href
                  ? 'bg-primary/10 text-primary'
                  : 'text-muted-foreground hover:bg-muted hover:text-foreground'
              )}
            >
              {link.icon}
              <span>{link.title}</span>
            </Link>
          ))}
          
          {roleSpecificLinks.length > 0 && (
            <>
              <div className="pt-4 pb-2">
                <div className="border-t" />
              </div>
              {roleSpecificLinks.map((link) => (
                <Link
                  key={link.href}
                  href={link.href}
                  className={cn(
                    'flex items-center space-x-2 px-3 py-2 rounded-md text-sm font-medium transition-colors',
                    pathname === link.href
                      ? 'bg-primary/10 text-primary'
                      : 'text-muted-foreground hover:bg-muted hover:text-foreground'
                  )}
                >
                  {link.icon}
                  <span>{link.title}</span>
                </Link>
              ))}
            </>
          )}
        </nav>
      </div>
    </aside>
  );
}